/** Convert 5x7 char to 7x5 grid (row-major, for drawing: grid[row][col]) */
export function charToGrid(char) {
  const upper = String(char).toUpperCase()
  const glyph = FONT_5X7[upper] ?? FONT_5X7[' ']
  const grid = []
  for (let r = 0; r < 7; r++) {
    const row = []
    for (let c = 0; c < 5; c++) {
      row.push(glyph[r][c] ?? 0)
    }
    grid.push(row)
  }
  return grid
}

/** Full string to a single bitmap: 7 rows, (5+1)*len cols (5 px + 1 space per char) */
export function stringToGrid(str) {
  if (!str) return []
  const rows = []
  for (let r = 0; r < 7; r++) rows.push([])
  for (let i = 0; i < str.length; i++) {
    const g = charToGrid(str[i])
    for (let r = 0; r < 7; r++) {
      rows[r].push(...g[r], 0)
    }
  }
  return rows
}
